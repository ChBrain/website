#!/usr/bin/env node
// Prebuild: pack each installed engine and each khai skill into a zip artifact,
// then write the zip + a sidecar JSON to public/downloads/{engines,skills}/.
// The sidecar carries everything the pages need to render the download
// back-cover spread (filename, size, sha256, and for skills the bodyHtml).
//
// Runs via `npm run prebuild` (before astro build). When @chbrain packages are
// not installed (CI sandbox, no registry token) the script exits 0 cleanly so
// astro build can continue without generating download artifacts.

import { readFileSync, writeFileSync, mkdirSync, readdirSync, existsSync, statSync } from "node:fs";
import { join, dirname } from "node:path";
import { createRequire } from "node:module";
import MarkdownIt from "markdown-it";

const _require = createRequire(import.meta.url);
const md = new MarkdownIt({ html: false, breaks: false, linkify: false });

// Minimal frontmatter parse (data + body) over a leading --- block, on js-yaml 4
// (the patched 4.2.0 already in the tree) rather than gray-matter, whose pinned
// js-yaml 3.x carries GHSA-h67p-54hq-rp68. Mirrors src/lib/frontmatter.ts for
// this script's plain-node runtime.
const { load: loadYaml } = _require("js-yaml");
function frontmatter(input) {
  const text = input.charCodeAt(0) === 0xfeff ? input.slice(1) : input;
  const match = /^---[ \t]*\r?\n([\s\S]*?)\r?\n---[ \t]*(?:\r?\n|$)/.exec(text);
  if (!match) return { data: {}, content: text };
  const yaml = match[1].trim();
  return { data: yaml === "" ? {} : (loadYaml(yaml) ?? {}), content: text.slice(match[0].length) };
}

function fmtBytes(n) {
  if (n < 1024) return `${n} B`;
  if (n < 1024 * 1024) return `${(n / 1024).toFixed(1)} kB`;
  return `${(n / 1024 / 1024).toFixed(1)} MB`;
}

function readMarkdownStripped(path) {
  const raw = readFileSync(path, "utf8");
  return frontmatter(raw).content.trim() + "\n";
}

// The engines a culture ships with are whatever khai-cultures declares in its
// npm dependency closure -- not a hand-kept list. Walk khai-cultures' @chbrain/*
// dependencies (recursively, so an engine that needs another rides along) and
// collect every @chbrain/khai-engine-* package. Today that is khai-engine-spine;
// when khai-cultures adds khai-engine-language (etc.) the build picks it up with
// no change here. Returns Map<engineId, packageDir>.
function culturesEngineClosure(rootPkgDir) {
  const engines = new Map();
  const seen = new Set();
  const visit = (pkgDir) => {
    let pkg;
    try {
      pkg = JSON.parse(readFileSync(join(pkgDir, "package.json"), "utf8"));
    } catch {
      return;
    }
    for (const dep of Object.keys(pkg.dependencies || {})) {
      if (seen.has(dep) || !dep.startsWith("@chbrain/")) continue;
      seen.add(dep);
      let depDir;
      try {
        depDir = dirname(_require.resolve(`${dep}/package.json`));
      } catch {
        continue;
      }
      if (dep.startsWith("@chbrain/khai-engine-")) {
        engines.set(dep.slice("@chbrain/khai-engine-".length), depDir);
      }
      visit(depDir);
    }
  };
  visit(rootPkgDir);
  return engines;
}

// An engine ships as prose + per-host folders, not as the JS the website reads:
// gather its files recursively, strip frontmatter from markdown, and drop the
// npm/build scaffolding (package.json, changelog) and any engine code.
const ENGINE_SKIP_ROOT = new Set(["package.json", "CHANGELOG.md"]);
function collectEngineFiles(dir, base = "") {
  const files = [];
  for (const f of readdirSync(dir)) {
    if (base === "" && ENGINE_SKIP_ROOT.has(f)) continue;
    if (/\.(mjs|cjs|js|ts|map)$/.test(f)) continue; // engine code, not world content
    const filePath = join(dir, f);
    const rel = base ? `${base}/${f}` : f;
    if (statSync(filePath).isDirectory()) {
      files.push(...collectEngineFiles(filePath, rel));
    } else if (f.endsWith(".md")) {
      files.push({ path: rel, data: readMarkdownStripped(filePath) });
    } else {
      files.push({ path: rel, data: readFileSync(filePath) });
    }
  }
  return files;
}

// The subdomain docroots (architecture/cultures) are nested inside
// /public_html/ and inherit the host's Apache config, which denies this
// build-generated subtree by default: a direct request for an existing
// downloads/*.zip returns 403, not the file. This .htaccess sits at the
// downloads/ root (so it covers both engines/ and skills/) and re-grants read
// access, suppresses directory listing, and serves .zip as a typed download.
// It is written into public/downloads/ here, then rides copy-public-to-surfaces
// + the deploy rsync (-a keeps the dotfile) into every surface.
const DOWNLOADS_HTACCESS = `# Generated by scripts/build-downloads.mjs. Do not edit by hand.
# Serve build-generated engine and skill zip artifacts as static downloads.

# Re-grant read access in case a parent/host rule denies this subtree (the
# subdomain docroots are nested inside /public_html/ and inherit its config).
<IfModule mod_authz_core.c>
  Require all granted
</IfModule>
<IfModule !mod_authz_core.c>
  Order allow,deny
  Allow from all
</IfModule>

# No directory listing; serve files directly.
Options -Indexes

# Type the zips and offer them as a download rather than letting any handler
# try to interpret them.
<IfModule mod_mime.c>
  AddType application/zip .zip
</IfModule>
<FilesMatch "\\.zip$">
  SetHandler default-handler
  <IfModule mod_headers.c>
    Header set Content-Disposition attachment
  </IfModule>
</FilesMatch>
`;

function writeDownloadsHtaccess() {
  const dir = join(process.cwd(), "public", "downloads");
  mkdirSync(dir, { recursive: true });
  writeFileSync(join(dir, ".htaccess"), DOWNLOADS_HTACCESS);
  console.log("  wrote downloads/.htaccess (grants read access + zip download headers)");
}

// Try loading @chbrain packages. If they are not installed (CI sandbox with no
// registry token) exit 0 so astro build can continue without downloads.
let packBundle, zipStore, sha256, buildAll;
try {
  ({ packBundle, zipStore, sha256 } = await import("@chbrain/khai-pack"));
  ({ buildAll } = await import("@chbrain/khai-skills/build"));
} catch (e) {
  console.log(`build-downloads: skipping — ${e.message}`);
  console.log("  Run npm run deps:sync on a machine with the GitHub Packages token.");
  process.exit(0);
}

// ── flat-for-host packaging ─────────────────────────────────────────────────
//
// Claude.ai project knowledge takes a flat set of files: no directory structure,
// and a link into another folder goes nowhere. So a culture zip is flattened --
// each file's folder path becomes a "_"-joined name prefix (world/persona_x.md
// -> world_persona_x.md, engines/spine/instructions.md ->
// engines_spine_instructions.md) -- and every relative markdown link is rewritten
// to the matching flat name, so the cross-references still resolve in one flat
// pile. External links (http/mailto), absolute paths, and bare #anchors are left
// alone.

function flattenName(logicalPath) {
  return logicalPath.split("/").join("_");
}

// Resolve a relative link target against the linking file's folder, collapsing
// "." and "..", to the bundle-logical path it points at.
function resolveLogical(fromDir, rel) {
  const parts = fromDir ? fromDir.split("/") : [];
  for (const seg of rel.split("/")) {
    if (seg === "" || seg === ".") continue;
    else if (seg === "..") parts.pop();
    else parts.push(seg);
  }
  return parts.join("/");
}

function rewriteLinksFlat(md, logicalPath) {
  const dir = logicalPath.includes("/") ? logicalPath.slice(0, logicalPath.lastIndexOf("/")) : "";
  // [text](target) and [text](target "title"): target is the first run with no
  // whitespace or ")"; an optional title tail is preserved verbatim.
  return md.replace(/\]\(([^)\s]+)([^)]*)\)/g, (whole, target, tail) => {
    if (/^(https?:|mailto:|tel:|#|\/)/i.test(target)) return whole;
    const hash = target.indexOf("#");
    const pathPart = hash >= 0 ? target.slice(0, hash) : target;
    const anchor = hash >= 0 ? target.slice(hash) : "";
    if (pathPart === "") return whole;
    return `](${flattenName(resolveLogical(dir, pathPart))}${anchor}${tail})`;
  });
}

// Flatten a logical bundle (files with forward-slash paths) into a flat zip:
// prefix-rename every file, rewrite links in markdown, store with zipStore.
function packFlat(name, logicalFiles) {
  const seen = new Set();
  const entries = [];
  for (const f of logicalFiles) {
    const flat = flattenName(f.path);
    if (seen.has(flat))
      throw new Error(`packFlat(${name}): duplicate flat name "${flat}" (from ${f.path})`);
    seen.add(flat);
    let data = f.data;
    if (f.path.endsWith(".md") && typeof data === "string") data = rewriteLinksFlat(data, f.path);
    entries.push({ name: flat, data: Buffer.isBuffer(data) ? data : Buffer.from(String(data)) });
  }
  const zip = zipStore(entries);
  return { zip, zipSha256: sha256(zip) };
}

// ── engines ────────────────────────────────────────────────────────────────

function buildEngineDownloads() {
  const scope = join(process.cwd(), "node_modules", "@chbrain");
  if (!existsSync(scope)) return;

  let archDir;
  try {
    archDir = dirname(_require.resolve("@chbrain/khai-arch/package.json"));
  } catch {
    console.log("  engines: @chbrain/khai-arch not found; skipping");
    return;
  }

  const outDir = join(process.cwd(), "public", "downloads", "engines");
  mkdirSync(outDir, { recursive: true });

  let count = 0;
  for (const name of readdirSync(scope).sort()) {
    if (!name.startsWith("khai-engine-")) continue;
    const id = name.slice("khai-engine-".length);
    const dir = join(scope, name);

    // Meta engines (e.g. the spine) are structural, not standalone bundles --
    // they ship inside the culture zips that depend on them, not on the engine
    // shelf. Skip them here so there is no orphan zip the shelf never links.
    const engineClass = JSON.parse(readFileSync(join(dir, "package.json"), "utf8")).khai?.class;
    if (engineClass === "meta") continue;

    // Identify overhead files.
    // Overhead: package.json, README.md, REFERENCES.md, LICENSE, LICENSE-CODE.
    const overhead = [{ path: "package.json", data: readFileSync(join(dir, "package.json")) }];

    const readmePath = join(dir, "README.md");
    if (existsSync(readmePath)) {
      overhead.push({ path: "README.md", data: readMarkdownStripped(readmePath) });
    }

    const refPath = join(dir, "REFERENCES.md");
    if (existsSync(refPath)) {
      overhead.push({ path: "REFERENCES.md", data: readMarkdownStripped(refPath) });
    }

    // License files from @chbrain/khai-arch
    const licensePath = join(archDir, "LICENSE");
    if (existsSync(licensePath)) {
      overhead.push({ path: "LICENSE", data: readFileSync(licensePath) });
    }
    const licenseCodePath = join(archDir, "LICENSE-CODE");
    if (existsSync(licenseCodePath)) {
      overhead.push({ path: "LICENSE-CODE", data: readFileSync(licenseCodePath) });
    }

    // Content: all other markdown files (except package.json, README, REFERENCES, LICENSE, LICENSE-CODE, CHANGELOG).
    const contentFiles = [];
    for (const f of readdirSync(dir)) {
      if (!f.endsWith(".md")) continue;
      if (
        f === "README.md" ||
        f === "REFERENCES.md" ||
        f === "CHANGELOG.md" ||
        f === "LICENSE" ||
        f === "LICENSE-CODE"
      )
        continue;

      const filePath = join(dir, f);
      contentFiles.push({ path: f, data: readMarkdownStripped(filePath) });
    }

    if (overhead.length === 0) continue;

    const packed = packBundle({
      name: id,
      overhead,
      content: {
        dir: "content",
        files: contentFiles,
      },
      stamp: { kind: "engine", engine: id },
    });

    writeFileSync(join(outDir, `${id}.zip`), packed.zip);
    writeFileSync(
      join(outDir, `${id}.json`),
      JSON.stringify({
        filename: `${id}.zip`,
        size: fmtBytes(packed.zip.length),
        sha256: packed.zipSha256,
      }) + "\n",
    );
    console.log(
      `  engine ${id}: ${fmtBytes(packed.zip.length)} sha256=${packed.zipSha256.slice(0, 12)}…`,
    );
    count++;
  }
  if (count === 0) console.log("  engines: none installed");
}

// ── skills ─────────────────────────────────────────────────────────────────

function buildSkillDownloads() {
  let skillsPkg;
  try {
    skillsPkg = dirname(_require.resolve("@chbrain/khai-skills/package.json"));
  } catch {
    console.log("  skills: @chbrain/khai-skills not found");
    return;
  }

  const outDir = join(process.cwd(), "public", "downloads", "skills");
  mkdirSync(outDir, { recursive: true });

  // buildAll with write:true composes each skill and writes zips to
  // khai-skills/dist/ inside the installed package. We then copy them to
  // public/downloads/skills/ alongside a sidecar JSON the skillbook page reads.
  const { results } = buildAll({ root: skillsPkg, write: true });
  const distRoot = join(skillsPkg, "dist");

  for (const r of results) {
    if (r.errors.length > 0) {
      console.error(`  skill ${r.name}: errors — ${r.errors.join("; ")}`);
      continue;
    }

    const zip = readFileSync(join(distRoot, `${r.name}.zip`));
    writeFileSync(join(outDir, `${r.name}.zip`), zip);

    // The sidecar carries the title, rendered body (for the content spread),
    // and download metadata (for the download spread).
    const skillMdPath = join(skillsPkg, "src", r.name, "SKILL.md");
    const { data, content } = frontmatter(readFileSync(skillMdPath, "utf8"));
    const h1 = content.match(/^# (.+)$/m);
    const title = h1 ? h1[1] : data.name;
    // Strip the H1: the spread chassis already renders the title; a second
    // heading in bodyHtml would duplicate it.
    const bodyWithoutH1 = content.replace(/^# .+\n+/, "");
    // Strip relative .md links — they reference bundle files that aren't pages.
    // Replace with the link text so prose reads naturally without broken hrefs.
    const bodyMd = bodyWithoutH1.replace(/\[([^\]]+)\]\((?!https?:\/\/|#)[^)]*\.md\)/g, "$1");
    const bodyHtml = md.render(bodyMd);

    // Extract Problem / Solution / What you get sections from README.md
    // (each is the content between its ## heading and the next ## or EOF).
    let problem = null,
      solution = null,
      outcome = null;
    const readmePath = join(skillsPkg, "src", r.name, "README.md");
    if (existsSync(readmePath)) {
      const readmeSrc = readFileSync(readmePath, "utf8");
      const extractSection = (src, heading) => {
        // Capture from "## <heading>" to the NEXT "## " heading or end of file,
        // then flatten the section's hard wraps to single spaces. The terminator
        // must be the next heading or true EOF -- NOT end-of-line. The earlier
        // /(?=^## |$)/m stopped at the first line break, because in multiline
        // mode `$` matches every line end: a hard-wrapped paragraph lost
        // everything after its first line, while a single-line one survived
        // whole. That is exactly why khai-playwright (one long line) read in full
        // and khai-impresario (wrapped) cut off mid-sentence.
        const re = new RegExp(`\\n## ${heading}[ \\t]*\\n([\\s\\S]*?)(?=\\n## |$)`);
        const m = `\n${src}`.match(re);
        return m ? m[1].trim().replace(/\s*\n\s*/g, " ") : null;
      };
      problem = extractSection(readmeSrc, "Problem");
      solution = extractSection(readmeSrc, "Solution");
      outcome = extractSection(readmeSrc, "What you get");
    }

    writeFileSync(
      join(outDir, `${r.name}.json`),
      JSON.stringify({
        name: data.name,
        slug: data.name,
        title,
        bodyHtml,
        filename: `${r.name}.zip`,
        size: fmtBytes(zip.length),
        sha256: r.zipSha,
        problem,
        solution,
        outcome,
      }) + "\n",
    );
    console.log(
      `  skill ${r.name}: ${fmtBytes(zip.length)} sha256=${(r.zipSha ?? "?").slice(0, 12)}…`,
    );
  }
}

// ── plays ──────────────────────────────────────────────────────────────────

async function buildPlayDownloads() {
  let loadRegistry;
  try {
    ({ loadRegistry } = await import("@chbrain/khai-plays"));
  } catch (e) {
    console.log(`  plays: skipping plays build — ${e.message}`);
    return;
  }

  const outDir = join(process.cwd(), "public", "downloads", "plays");
  mkdirSync(outDir, { recursive: true });

  const houses = loadRegistry();
  for (const house of houses) {
    let houseDir;
    try {
      houseDir = dirname(_require.resolve(`${house.package}/package.json`));
    } catch {
      console.log(`  plays: house package ${house.package} not found; skipping`);
      continue;
    }

    const playsDir = join(houseDir, "plays");
    if (!existsSync(playsDir)) continue;

    const houseOutDir = join(outDir, house.id);
    mkdirSync(houseOutDir, { recursive: true });

    // Pack the entire house package into a ZIP file
    const houseContentFiles = [];
    for (const playName of readdirSync(playsDir)) {
      const playDir = join(playsDir, playName);
      if (!statSync(playDir).isDirectory()) continue;
      for (const f of readdirSync(playDir)) {
        const filePath = join(playDir, f);
        const relativePath = `${playName}/${f}`;
        if (f.endsWith(".md")) {
          houseContentFiles.push({ path: relativePath, data: readMarkdownStripped(filePath) });
        } else {
          houseContentFiles.push({ path: relativePath, data: readFileSync(filePath) });
        }
      }
    }
    const houseOverhead = [
      { path: "package.json", data: readFileSync(join(houseDir, "package.json")) },
      { path: "README.md", data: readMarkdownStripped(join(houseDir, "README.md")) },
      { path: "LICENSE", data: readFileSync(join(houseDir, "LICENSE")) },
    ];
    const houseLicenseCodePath = join(houseDir, "LICENSE-CODE");
    if (existsSync(houseLicenseCodePath)) {
      houseOverhead.push({ path: "LICENSE-CODE", data: readFileSync(houseLicenseCodePath) });
    }
    const housePacked = packBundle({
      name: house.id,
      overhead: houseOverhead,
      content: {
        dir: "plays",
        files: houseContentFiles,
      },
      stamp: { kind: "house", house: house.id },
    });
    // Write house output
    writeFileSync(join(outDir, `${house.id}.zip`), housePacked.zip);
    writeFileSync(
      join(outDir, `${house.id}.json`),
      JSON.stringify({
        filename: `${house.id}.zip`,
        size: fmtBytes(housePacked.zip.length),
        sha256: housePacked.zipSha256,
      }) + "\n",
    );
    console.log(
      `  house ${house.id}: ${fmtBytes(housePacked.zip.length)} sha256=${housePacked.zipSha256.slice(0, 12)}…`,
    );

    for (const playName of readdirSync(playsDir)) {
      const playDir = join(playsDir, playName);
      if (!statSync(playDir).isDirectory()) continue;

      // Find files under playDir
      const files = readdirSync(playDir);
      const contentFiles = [];
      for (const f of files) {
        if (f === "package.json") continue;
        if (f === "REFERENCES.md") continue;
        const filePath = join(playDir, f);
        if (f.endsWith(".md")) {
          contentFiles.push({ path: f, data: readMarkdownStripped(filePath) });
        } else {
          contentFiles.push({ path: f, data: readFileSync(filePath) });
        }
      }

      // Overhead: house README and LICENSE
      const overhead = [
        { path: "README.md", data: readMarkdownStripped(join(houseDir, "README.md")) },
        { path: "LICENSE", data: readFileSync(join(houseDir, "LICENSE")) },
      ];
      const licenseCodePath = join(houseDir, "LICENSE-CODE");
      if (existsSync(licenseCodePath)) {
        overhead.push({ path: "LICENSE-CODE", data: readFileSync(licenseCodePath) });
      }
      // Add REFERENCES.md to overhead at the root of the play bundle if it exists
      const refPath = join(playDir, "REFERENCES.md");
      if (existsSync(refPath)) {
        overhead.push({ path: "REFERENCES.md", data: readMarkdownStripped(refPath) });
      }

      // Pack the bundle
      const packed = packBundle({
        name: playName,
        overhead,
        content: {
          dir: "content",
          files: contentFiles,
        },
        stamp: { kind: "play", house: house.id, play: playName },
      });

      // Write output
      writeFileSync(join(houseOutDir, `${playName}.zip`), packed.zip);
      writeFileSync(
        join(houseOutDir, `${playName}.json`),
        JSON.stringify({
          filename: `${playName}.zip`,
          size: fmtBytes(packed.zip.length),
          sha256: packed.zipSha256,
        }) + "\n",
      );

      console.log(
        `  play ${house.id}/${playName}: ${fmtBytes(packed.zip.length)} sha256=${packed.zipSha256.slice(0, 12)}…`,
      );
    }
  }
}

// ── cultures ─────────────────────────────────────────────────────────────────
//
// Each installed culture (@chbrain/khai-cultures · cultures/<id>/) is packed
// into a download zip + sidecar, exactly like a play house. Alongside the zips
// this also writes the *map manifest* — public/downloads/cultures/available.json
// (schemaVersion 2): a kind-discriminated `entries[]` union the /cultures/map
// page consumes. Culture entries carry their ISO anchor + asset; group entries
// carry references to member cultures (a lens + collection, no geo, no zip).
//
// The producer emits facts only: id, title, description, and the ISO code from
// the registry (geo.json is an optional override carrying an explicit `parent`).
// The page derives the drill-down tree (DE-SH -> DE), country names, and region
// colours from the ISO code. A culture with no ISO is listed but not placed.
//
// downloadBase is site-relative ("/downloads/cultures/") because these zips are
// served from the cultures surface itself, NOT GitHub releases. The legacy
// schema wants an absolute https URL ending in "/"; a site-relative base keeps
// the consumer's `downloadBase + asset` join correct while pointing at the zips
// this producer writes.

function buildCultureDownloads() {
  let culturesPkgDir, registry;
  try {
    culturesPkgDir = dirname(_require.resolve("@chbrain/khai-cultures/package.json"));
    registry = JSON.parse(readFileSync(join(culturesPkgDir, "registry.json"), "utf8"));
  } catch (e) {
    console.log(`  cultures: @chbrain/khai-cultures not found; skipping — ${e.message}`);
    return;
  }

  const culturesDir = join(culturesPkgDir, "cultures");
  if (!existsSync(culturesDir)) {
    console.log("  cultures: no cultures/ directory; skipping");
    return;
  }

  const outDir = join(process.cwd(), "public", "downloads", "cultures");
  mkdirSync(outDir, { recursive: true });

  const pkgVersion = (() => {
    try {
      return JSON.parse(readFileSync(join(culturesPkgDir, "package.json"), "utf8")).version;
    } catch {
      return registry.version || "unknown";
    }
  })();

  // House-level overhead reused for every culture bundle: the package README and
  // licences (content CC-BY-NC-SA + code MIT). Mirrors the play bundle shape.
  const cultureOverhead = [];
  const houseReadme = join(culturesPkgDir, "README.md");
  if (existsSync(houseReadme)) {
    cultureOverhead.push({ path: "README.md", data: readMarkdownStripped(houseReadme) });
  }
  const houseLicense = join(culturesPkgDir, "LICENSE");
  if (existsSync(houseLicense)) {
    cultureOverhead.push({ path: "LICENSE", data: readFileSync(houseLicense) });
  }
  const houseLicenseCode = join(culturesPkgDir, "LICENSE-CODE");
  if (existsSync(houseLicenseCode)) {
    cultureOverhead.push({ path: "LICENSE-CODE", data: readFileSync(houseLicenseCode) });
  }

  const entries = [];
  const cultures = Array.isArray(registry.cultures) ? registry.cultures : [];
  const groups = Array.isArray(registry.groups) ? registry.groups : [];

  // Each culture's content files, kept so a group (e.g. DACH) can be packed into
  // a single set zip its members live under (see the groups loop below).
  const memberFilesById = new Map();

  // The engines cultures depend on, bundled once and carried in every culture +
  // set zip under engines/<id>/, so a downloaded world is runnable on its own.
  // npm-derived (culturesEngineClosure) -- not hand-listed -- so the set tracks
  // whatever khai-cultures declares.
  const engineClosure = culturesEngineClosure(culturesPkgDir);
  const engineOverhead = [];
  for (const [engineId, engineDir] of engineClosure) {
    for (const file of collectEngineFiles(engineDir)) {
      engineOverhead.push({ path: `engines/${engineId}/${file.path}`, data: file.data });
    }
  }
  if (engineClosure.size > 0) {
    console.log(`  cultures: engines bundled -> [${[...engineClosure.keys()].sort().join(", ")}]`);
  } else {
    console.log("  cultures: no engine dependencies declared; zips carry world/ only");
  }

  for (const entry of cultures) {
    const id = entry.id;
    const cultureDir = join(culturesDir, id);
    if (!existsSync(cultureDir) || !statSync(cultureDir).isDirectory()) {
      console.log(`  culture ${id}: no cultures/${id}/ directory; skipping`);
      continue;
    }

    // The culture's own files (its README, REFERENCES, personas, positions, ...)
    // stay together under world/ so their dense sibling cross-links resolve once
    // flattened; markdown is stripped of frontmatter, other files copied as-is.
    const worldFiles = [];
    for (const f of readdirSync(cultureDir)) {
      const filePath = join(cultureDir, f);
      if (!statSync(filePath).isFile()) continue;
      const data = f.endsWith(".md") ? readMarkdownStripped(filePath) : readFileSync(filePath);
      worldFiles.push({ path: f, data });
    }

    // Stored bundle-relative (under world/) so a set zip can re-home each member
    // under world/<member>/ (see the groups loop below).
    memberFilesById.set(id, worldFiles);

    // The bundle: house README/licences at the root, the engines under
    // engines/<id>/, the culture under world/ -- then flattened to one flat pile
    // with links rewritten, the shape Claude.ai project knowledge takes.
    const logicalFiles = [
      ...cultureOverhead,
      ...engineOverhead,
      ...worldFiles.map((f) => ({ path: `world/${f.path}`, data: f.data })),
    ];
    const packed = packFlat(id, logicalFiles);

    const size = fmtBytes(packed.zip.length);
    writeFileSync(join(outDir, `${id}.zip`), packed.zip);
    writeFileSync(
      join(outDir, `${id}.json`),
      JSON.stringify({ filename: `${id}.zip`, size, sha256: packed.zipSha256 }) + "\n",
    );
    console.log(`  culture ${id}: ${size} sha256=${packed.zipSha256.slice(0, 12)}...`);

    // ISO anchor: the registry carries it. geo.json is an optional override and
    // the only place an explicit deep-nesting `parent` is declared; the page
    // otherwise derives the country (DE-SH -> DE), the name, and the region
    // colour from the ISO code alone.
    let iso = typeof entry.iso === "string" ? entry.iso : null;
    let parent = null;
    const geoPath = join(cultureDir, "geo.json");
    if (existsSync(geoPath)) {
      try {
        const geo = JSON.parse(readFileSync(geoPath, "utf8"));
        if (typeof geo.iso === "string") iso = geo.iso;
        if (typeof geo.parent === "string") parent = geo.parent;
      } catch (e) {
        console.log(`  culture ${id}: geo.json unreadable (${e.message})`);
      }
    }

    const item = { kind: "culture", id, title: entry.title || id, asset: `${id}.zip`, size };
    if (entry.description) item.description = entry.description;
    if (iso) item.iso = iso;
    else console.log(`  culture ${id}: no iso; listed but not placed on the map`);
    if (parent) item.parent = parent;
    entries.push(item);
  }

  // Groups: referencing entries (e.g. DACH). A group is a collection that
  // downloads as ONE set: its members are packed together into a single
  // <id>.zip (each member under its own folder), so the page can offer the
  // whole set in one go alongside the per-member links. The group is also a
  // lens over its member countries on the map. No geo of its own.
  for (const g of groups) {
    const references = Array.isArray(g.references) ? g.references : [];
    const item = {
      kind: "group",
      id: g.id,
      title: g.title || g.id,
      ...(g.description ? { description: g.description } : {}),
      references,
    };

    // Gather every member culture, re-homed under world/<member>/, then pack the
    // lot into one flat set zip: house README/licences at the root, the engines
    // once under engines/<id>/, the members under world/<member>/.
    const worldFiles = [];
    const present = [];
    for (const memberId of references) {
      const files = memberFilesById.get(memberId);
      if (!files) continue;
      present.push(memberId);
      for (const f of files) worldFiles.push({ path: `world/${memberId}/${f.path}`, data: f.data });
    }

    if (worldFiles.length > 0) {
      const logicalFiles = [...cultureOverhead, ...engineOverhead, ...worldFiles];
      const packed = packFlat(g.id, logicalFiles);
      const size = fmtBytes(packed.zip.length);
      writeFileSync(join(outDir, `${g.id}.zip`), packed.zip);
      writeFileSync(
        join(outDir, `${g.id}.json`),
        JSON.stringify({ filename: `${g.id}.zip`, size, sha256: packed.zipSha256 }) + "\n",
      );
      item.asset = `${g.id}.zip`;
      item.size = size;
      console.log(
        `  group ${g.id}: ${size} (${present.length} members) sha256=${packed.zipSha256.slice(0, 12)}...`,
      );
    } else {
      console.log(`  group ${g.id}: no member packages present; set zip skipped`);
    }

    entries.push(item);
  }

  const manifest = {
    schemaVersion: 2,
    release: pkgVersion,
    downloadBase: "/downloads/cultures/",
    entries,
  };
  writeFileSync(join(outDir, "available.json"), JSON.stringify(manifest, null, 2) + "\n");
  const placed = entries.filter((e) => e.kind === "culture" && e.iso).length;
  console.log(`  cultures: wrote available.json (${entries.length} entries, ${placed} placed)`);
}

